#include "food.h"
#include <cstdlib>

food::food() {}

void food::init(int speed) {
    //////printf("food: Init\n");
    int randomx = rand()%85; //x value 0-84
    _velocity.x = speed;
    _velocity.y = speed;
    if(randomx >= 72) _x = 72; //the food sprite is 12
    //84-12 = 72
    else if(randomx <= 0) _x = 0;
    else _x = randomx;
    _y = 0;
    _size = 12;
}

void food::draw(N5110 &lcd) {
    ////printf("food: Draw\n");
    lcd.drawSprite(_x,_y,_size,_size,(int *)food_sprite);
}

void food::update(){
    ////printf("food: Update\n");
    _x += 0;
    _y += _velocity.y;
}

void food::set_velocity(Position2D v){
    //printf("food: Velocity\n");
    _velocity.x = v.x;
    _velocity.y = v.y;
}

void food::set_pos(Position2D p) {
    //printf("food: Set Position\n");
    _x = p.x;
    _y = p.y;
}

Position2D food::get_velocity(){ return {_velocity.x,_velocity.y}; }

Position2D food::get_pos() { return {_x,_y}; }

int food::get_size() { return _size; }

